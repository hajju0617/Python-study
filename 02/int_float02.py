intput_a = float(input("첫 번째 숫자 : "))
intput_b = float(input("두 번째 숫자 : "))

print("덧셈 결과 : ", intput_a + intput_b)
print("뺄셈 결과 : ", intput_a - intput_b)
print("곱셈 결과 : ", intput_a * intput_b)
print("나눗셈 결과 : ", intput_a / intput_b)
print("정수 나눗셈 결과 : ", intput_a // intput_b)
print("나머지 연산자 결과 : ", intput_a % intput_b)